describe('BrandThemeLoader', () => {
  const loader = new BrandThemeLoader()

  it('lists built-in brands', () => {
    const brands = loader.listAvailableBrands()
    expect(brands).toContain('vercel')
    expect(brands).toContain('stripe')
    expect(brands).toContain('notion')
    expect(brands).toContain('linear')
    expect(brands).toContain('github')
  })

  it('loads vercel brand', () => {
    const theme = loader.loadBrand('vercel')
    expect(theme).not.toBeNull()
    expect(theme!.name).toBe('vercel')
    expect(theme!.displayName).toBe('Vercel')
    expect(theme!.colors.primary).toBe('#000000')
  })

  it('loads stripe brand', () => {
    const theme = loader.loadBrand('stripe')
    expect(theme).not.toBeNull()
    expect(theme!.colors.primary).toBe('#635bff')
    expect(theme!.colors.bg).toBe('#0a2540')
  })

  it('loads notion brand (light theme)', () => {
    const theme = loader.loadBrand('notion')
    expect(theme).not.toBeNull()
    expect(theme!.colors.bg).toBe('#ffffff')
    expect(theme!.colors.fg).toBe('#37352f')
  })

  it('loads github brand', () => {
    const theme = loader.loadBrand('github')
    expect(theme).not.toBeNull()
    expect(theme!.colors.primary).toBe('#0969da')
  })

  it('returns null for unknown brand', () => {
    const theme = loader.loadBrand('unknown-brand-12345')
    expect(theme).toBeNull()
  })

  it('hasBrand returns true for built-in brands', () => {
    expect(loader.hasBrand('vercel')).toBe(true)
    expect(loader.hasBrand('stripe')).toBe(true)
    expect(loader.hasBrand('nonexistent')).toBe(false)
  })

  it('generates CSS variables from brand theme', () => {
    const theme = loader.loadBrand('vercel')!
    const css = loader.getCSSVariables(theme)
    expect(css).toContain('--brand-primary:')
    expect(css).toContain('#000000')
    expect(css).toContain('--brand-bg:')
  })

  it('generates override CSS for brand', () => {
    const theme = loader.loadBrand('linear')!
    const css = loader.getOverrideCSS(theme)
    expect(css).toContain('--bg:')
    expect(css).toContain('--accent:')
  })

  it('loadBrand returns Linear theme', () => {
    const theme = loader.loadBrand('linear')
    expect(theme).not.toBeNull()
    expect(theme!.name).toBe('linear')
    expect(theme!.colors.primary).toBe('#5e6ad2')
  })
})

