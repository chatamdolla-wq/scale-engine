describe('UIPrototypeRenderer', () => {
  const renderer = new UIPrototypeRenderer()

  describe('renderWireframe', () => {
    it('renders empty state', () => {
      const html = renderer.renderWireframe([])
      expect(html).toContain('<!DOCTYPE html>')
      expect(html).toContain('Empty Prototype')
    })

    it('renders single page', () => {
      const html = renderer.renderWireframe([{
        name: 'Home',
        title: 'Home Page',
        layout: 'single-column',
        components: [
          { type: 'hero', title: 'Welcome', data: { subtitle: 'Get started' } },
          { type: 'card', title: 'Feature 1', data: { content: 'Description' } },
        ],
      }])
      expect(html).toContain('Home Page')
      expect(html).toContain('Welcome')
      expect(html).toContain('Feature 1')
    })

    it('renders multi-page with tabs', () => {
      const html = renderer.renderWireframe([
        { name: 'Page 1', title: 'First', layout: 'single-column', components: [{ type: 'card', title: 'Card A' }] },
        { name: 'Page 2', title: 'Second', layout: 'single-column', components: [{ type: 'card', title: 'Card B' }] },
      ])
      expect(html).toContain('Page 1')
      expect(html).toContain('Page 2')
      expect(html).toContain('proto-tab')
      expect(html).toContain('showPage')
    })

    it('renders table component', () => {
      const html = renderer.renderWireframe([{
        name: 'Data',
        title: 'Data Page',
        layout: 'single-column',
        components: [{
          type: 'table',
          title: 'Users',
          data: { headers: ['Name', 'Email'], rows: [['Alice', 'alice@x.com'], ['Bob', 'bob@x.com']] },
        }],
      }])
      expect(html).toContain('Users')
      expect(html).toContain('Name')
      expect(html).toContain('Alice')
      expect(html).toContain('bob@x.com')
    })

    it('renders form component', () => {
      const html = renderer.renderWireframe([{
        name: 'Form',
        title: 'Registration',
        layout: 'single-column',
        components: [{
          type: 'form',
          title: 'Sign Up',
          data: { fields: [{ label: 'Email', type: 'email', placeholder: 'Enter email' }] },
        }],
      }])
      expect(html).toContain('Sign Up')
      expect(html).toContain('Enter email')
    })

    it('renders list component', () => {
      const html = renderer.renderWireframe([{
        name: 'List',
        title: 'Items',
        layout: 'single-column',
        components: [{
          type: 'list',
          title: 'Features',
          data: { items: ['Auth', 'Dashboard', 'API'] },
        }],
      }])
      expect(html).toContain('Features')
      expect(html).toContain('Auth')
      expect(html).toContain('Dashboard')
    })

    it('renders two-column layout', () => {
      const html = renderer.renderWireframe([{
        name: 'TwoCol',
        title: 'Two Column',
        layout: 'two-column',
        components: [{ type: 'card', title: 'Left' }, { type: 'card', title: 'Right' }],
      }])
      expect(html).toContain('proto-layout-2col')
    })

    it('renders sidebar layout', () => {
      const html = renderer.renderWireframe([{
        name: 'Sidebar',
        title: 'Sidebar Layout',
        layout: 'sidebar',
        components: [],
        navigation: [
          { label: 'Home', href: '/', active: true },
          { label: 'Settings', href: '/settings' },
        ],
      }])
      expect(html).toContain('proto-topnav')
      expect(html).toContain('Home')
      expect(html).toContain('Settings')
    })
  })

  describe('renderDashboard', () => {
    it('renders dashboard with widgets', () => {
      const html = renderer.renderDashboard({
        title: 'Admin Dashboard',
        description: 'System overview',
        nav: [
          { label: 'Overview', href: '/', active: true },
          { label: 'Users', href: '/users' },
        ],
        widgets: [
          { title: 'Total Users', type: 'metric', data: { value: '1,234', label: 'users' } },
          { title: 'Status', type: 'status', data: { ok: true, message: 'All systems operational' } },
          { title: 'Recent', type: 'list', data: { items: ['Event A', 'Event B'] }, size: 'large' },
        ],
      })
      expect(html).toContain('Admin Dashboard')
      expect(html).toContain('proto-sidebar')
      expect(html).toContain('Total Users')
      expect(html).toContain('1,234')
      expect(html).toContain('All systems operational')
      expect(html).toContain('widget-lg')
    })
  })

  describe('renderComponent', () => {
    it('renders component collection', () => {
      const html = renderer.renderComponent([
        { type: 'header', title: 'Page Header' },
        { type: 'card', title: 'Info Card', data: { content: 'Details here' } },
      ])
      expect(html).toContain('Page Header')
      expect(html).toContain('Info Card')
      expect(html).toContain('Details here')
    })
  })

  describe('theme options', () => {
    it('supports light theme', () => {
      const lightRenderer = new UIPrototypeRenderer({ theme: 'light' })
      const html = lightRenderer.renderWireframe([{
        name: 'Light', title: 'Light Page', layout: 'single-column', components: [],
      }])
      expect(html).toContain('#ffffff')
      expect(html).toContain('#1a1a1a')
    })

    it('supports custom accent color', () => {
      const customRenderer = new UIPrototypeRenderer({ accentColor: '#ff6b35' })
      const html = customRenderer.renderDashboard({
        title: 'Custom', nav: [], widgets: [{ title: 'M', type: 'metric', data: { value: '42', label: 'x' } }],
      })
      expect(html).toContain('#ff6b35')
    })
  })

  describe('escapes HTML', () => {
    it('escapes special characters in content', () => {
      const html = renderer.renderWireframe([{
        name: 'XSS',
        title: 'Test <script>',
        layout: 'single-column',
        components: [{ type: 'card', title: '<img onerror="alert(1)">' }],
      }])
      // Angle brackets should be escaped, making script injection impossible
      expect(html).not.toContain('<script>')
      expect(html).toContain('&lt;script&gt;')
      expect(html).toContain('&lt;img')
      expect(html).toContain('&gt;')
      // Quotes should be escaped
      expect(html).toContain('&quot;')
    })
  })
})
